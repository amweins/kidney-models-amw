	SUBROUTINE SDHLRESA
C
	INTEGER SOLS,X,T,CHOP
C
C GENERAL PARAMETERS
	DOUBLE PRECISION
     1   Z(15),RT,RTE,F,DIST,
     1   EPSI,DT,RTAU,TIME,L0,L(1601,2),
     1   PKC,PKF,PKN,PKP,KHY(5),KDHY(5),BCO2(3)
C LUMINAL AND PERITUBULAR PARAMETERS
	DOUBLE PRECISION
     1   VM(1601,2),PM(1601,2),CM(15,1601,2),
     1   IMPM(1601,2),LCHM(1601),XM(15,1601),
     1   VS(1601,2),PS(1601,2),CS(15,1601,2),
     1   IMPS(1601,2),LCHS(1601),XS(15,1601),
     1   TL,DX,RM0,MUM,ETA,ZIMPS,
     1   SM(1601,2),AM(1601,2),FVM(1601,2),FKM(16,1601,2)
C INTERSPACE PARAMETERS
	DOUBLE PRECISION
     1   AME,AE0,AE(1601,2),MUA,CHVL0,CHVL(1601,2),MUV,
     1   LPME,LPES,SME(15),SES(15),
     1   HME(15),HES(15),CME(15),CES(15),
     1   VE(1601,2),PE(1601,2),CE(15,1601,2),LCHE(1601),XE(15,1601),
     1   FEVM(1601,2),FEKM(15,1601,2),FEVS(1601,2),FEKS(15,1601,2),
     1   CURE(1601)
C CELL PARAMETERS
	DOUBLE PRECISION
     1   AIE(3),AMI(3),AIS(3),AI0(3),CLVL0(3),IMP0(3),CLVL(3,1601,2),
     1   ZIMP(3),TBUF(3),PKB(3),CBUF(3,1601,2),HCBUF(3,1601,2),
     1   LPMI(3),LPIS(3),SMI(3,15),SIS(3,15),
     1   HMI(3,15),HIS(3,15),CMI(3,15),CIE(3,15),CIS(3,15),
     1   LMI(3,15,15),LIS(3,15,15),
     1   ATMI(3,15,1601),ATIS(3,15,1601),ATIE(3,15,1601),
     1   VI(3,1601,2),PI(3,1601,2),CI(3,15,1601,2),IMP(3,1601),
     1   LCHI(3,1601),XI(3,15,1601),FIVM(3,1601,2),FIKM(3,15,1601,2),
     1   FIVS(3,1601,2),FIKS(3,15,1601,2),CURI(3,1601),JV(3,1601,2),
     1   JK(3,15,1601,2)
C SPECIAL TRANSPORTERS
	CHARACTER*1 ISOFM
	DOUBLE PRECISION
     1   NP(3),KNPN(3),KNPK(3),KNH4(3),NPHK(3),
     1   LHP(3),XIHP(3),XHP(3),NAE1(3),NTSC(3),NNHE3(3),
     1   JNAK(3,3,1601,2),JHK(3,1601,2),JHP(3,1601,2),
     1   JAE1(3,1601,2),JTSC(3,1601,2),JNHE3(3,3,1601,2),QIAMM,
     1   NNKCC(3),NKCL(3),JNKCC(3,4,1601,2),JKCC(3,3,1601,2)
C
        COMMON SOLS,T,X,CHOP,
     1   Z,RT,RTE,F,
     1   EPSI,DT,RTAU,TIME,DIST,
     1   PKC,PKF,PKN,PKP,KHY,KDHY,BCO2,L0,L,
     1   VM,PM,CM,IMPM,LCHM,XM,
     1   VS,PS,CS,IMPS,LCHS,XS,
     1   TL,DX,RM0,MUM,ETA,ZIMPS,
     1   SM,AM,FVM,FKM,
     1   AME,AE0,AE,MUA,CHVL0,CHVL,MUV,
     1   LPME,LPES,SME,SES,
     1   HME,HES,CME,CES,
     1   VE,PE,CE,LCHE,XE,
     1   FEVM,FEKM,FEVS,FEKS,CURE,
     1   AIE,AMI,AIS,AI0,CLVL0,IMP0,CLVL,
     1   ZIMP,TBUF,PKB,CBUF,HCBUF,
     1   LPMI,LPIS,SMI,SIS,
     1   HMI,HIS,CMI,CIE,CIS,
     1   LMI,LIS,ATMI,ATIS,ATIE,
     1   VI,PI,CI,IMP,LCHI,XI,
     1   FIVM,FIKM,FIVS,FIKS,CURI,
     1   JV,JK
	COMMON/KINET/ ISOFM,
     1   NP,KNPN,KNPK,KNH4,NPHK,
     1   LHP,XIHP,XHP,NAE1,NTSC,NNHE3,
     1   JNAK,JHK,JHP,JAE1,JTSC,JNHE3,QIAMM,
     1   NNKCC,NKCL,JNKCC,JKCC
C
C  SOLUTE INDEX:
C	1-	NA+
C	2-	K+
C	3-	CL-
C	4-	HCO3-
C	5-	H2CO3
C	6-	CO2
C	7-	HPO4--
C	8-	H2PO4-
C	9-	UREA
C	10-	NH3
C	11-	NH4+
C	12-	H+
C
	REAL 
     1    PFME,PFMI(3),PFIE(3),PFES,PFIS(3),
     1    HAME(12),HAMI(3,12),HAIE(3,12),HAES(12),HAIS(3,12),
     1    GME(12),GMI(3,12),GIE(3,12),GES(12),GIS(3,12),
     1    TGME,TGMI(3),TGIE(3),TGES,TGIS(3),HMI11
C
C 	G.(I)-	AREA ADJUSTED PARTIAL CONDUCTANCE (MHO/CM2)
C 	TG.-	AREA ADJUSTED TOTAL CONDUCTANCE (MHO/CM2)
C
	CHARACTER*5 SOL(12),CELL(3)
	SOL(1)='  NA '
	SOL(2)='  K  '
	SOL(3)='  CL '
	SOL(4)=' HCO3'
	SOL(5)='H2CO3'
	SOL(6)=' CO2 '
	SOL(7)=' HPO4'
	SOL(8)='H2PO4'
	SOL(9)=' UREA'
	SOL(10)=' NH3 '
	SOL(11)=' NH4 '
	SOL(12)='  H  '
C
	CELL(1)='PRINC'
	CELL(2)='ALPHA'
	CELL(3)='BETA '
C
	DO 100 I=1,SOLS
	CME(I)=(CE(I,X,T)-CM(I,X,T))
	IF (CME(I)) 147,146,147
  146   CME(I)=CM(I,X,T)
	GO TO 151
  147   CME(I)=CME(I)/DLOG(CE(I,X,T)/CM(I,X,T))
  151   DO 160 K=1,1
	CMI(K,I)=(CS(I,X,T)-CM(I,X,T))
	IF (CMI(K,I)) 153,152,153
  152   CMI(K,I)=CM(I,X,T)
	GO TO 160
  153   CMI(K,I)=CMI(K,I)/DLOG(CS(I,X,T)/CM(I,X,T))
  160   CONTINUE
  100   CONTINUE
C
C THIS IS A STEADY STATE SOLUTION
C     OR THE FIRST PASS OF A TIMED EXPERIMENT
C
	PFME=LPME*AME/.018
	DO 350 K=1,1
	PFMI(K)=LPMI(K)*AI0(K)/.018
  350   CONTINUE
C
	TGME=0.D0
	DO 355 I=1,SOLS
	HAME(I)=HME(I)*AME
C
	GME(I)=1.D-6*((F*Z(I))**2)*HAME(I)*CME(I)/RTE
C
	TGME=TGME+GME(I)
  355   CONTINUE
C
C
	DO 370 K=1,1
	TGMI(K)=0.D0
C
	DO 365 I=1,SOLS
	HAMI(K,I)=HMI(K,I)*AI0(K)
C
	GMI(K,I)=1.D-6*((F*Z(I))**2)*HAMI(K,I)*CMI(K,I)/RTE
C
	TGMI(K)=TGMI(K)+GMI(K,I)
  365   CONTINUE
  370   CONTINUE
C
C
	WRITE (121,372) RTE,F,PKC,PKP,PKN,KHY(5),KDHY(5)
	WRITE (121,374) AME,AI0(1)
C
  372   FORMAT (1H1,//,5X,'RTE=',F4.2,7X,
     1  'F=',D10.2,5X,'PKC=',F4.2,7X,'PKP=',F4.2,7X,'PKN=',F4.2,7X,
     1  'KHY=',D11.3,5X,'KDHY=',D11.3)
  374   FORMAT (/,5X,'AME=',D10.2,5X,'AI0=',D10.2)
C
	WRITE (121,380)
  380   FORMAT (//)
C
	WRITE (121,381) PFME,(PFMI(K),K=1,1)
	WRITE (121,384)
	WRITE (121,385) (SOL(I),SME(I),(SMI(K,I),K=1,1),I=1,SOLS)
	WRITE (121,386)
	WRITE (121,385) (SOL(I),HAME(I),(HAMI(K,I),K=1,1),I=1,SOLS)
	WRITE (121,388)
	WRITE (121,385) (SOL(I),GME(I),(GMI(K,I),K=1,1),I=1,SOLS)
	WRITE (121,389) TGME,(TGMI(K),K=1,1)
	WRITE (121,390) 1./TGME,(1./TGMI(K),K=1,1)
C
  381   FORMAT (/,14X,'PFME',7X,'PFMI',/,9X,2D11.4)
  384   FORMAT (//,15X,'SME',8X,'SMI')
  385   FORMAT (3X,A5,1X,2D11.4)
  386   FORMAT (//,15X,'HME',8X,'HMI')
  388   FORMAT (//,15X,'GME',8X,'GMI')
  389   FORMAT (/,3X,'TOTAL',1X,2D11.4)
  390   FORMAT (/,3X,'RESIS',1X,2D11.4)
C
C
 	RETURN
	END
