	SUBROUTINE SDHLRESF
C A NEW SUBROUTINE THAT OUTPUTS SOLUTE FLUXES POINTWISE - SORTED BY SPECIES
C BASED ON FRESUL IN DHL DIRECTORY
C
	INTEGER SOLS,X,T,CHOP
C
C GENERAL PARAMETERS
	DOUBLE PRECISION
     1   Z(15),RT,RTE,F,DIST,
     1   EPSI,DT,RTAU,TIME,L0,L(1601,2),
     1   PKC,PKF,PKN,PKP,KHY(5),KDHY(5)
C LUMINAL AND PERITUBULAR PARAMETERS
	DOUBLE PRECISION
     1   VM(1601,2),PM(1601,2),CM(15,1601,2),
     1   IMPM(1601,2),LCHM(1601),XM(15,1601),
     1   VS(1601,2),PS(1601,2),CS(15,1601,2),
     1   IMPS(1601,2),LCHS(1601),XS(15,1601),
     1   TL,DX,RM0,MUM,ETA,ZIMPS,
     1   SM(1601,2),AM(1601,2),FVM(1601,2),FKM(16,1601,2)
C INTERSPACE PARAMETERS
	DOUBLE PRECISION
     1   AME,AE0,AE(1601,2),MUA,CHVL0,CHVL(1601,2),MUV,
     1   LPME,LPES,SME(15),SES(15),
     1   HME(15),HES(15),CME(15),CES(15),
     1   VE(1601,2),PE(1601,2),CE(15,1601,2),LCHE(1601),XE(15,1601),
     1   FEVM(1601,2),FEKM(15,1601,2),FEVS(1601,2),FEKS(15,1601,2),
     1   CURE(1601)
C CELL PARAMETERS
	DOUBLE PRECISION
     1   AIE(3),AMI(3),AIS(3),AI0(3),CLVL0(3),IMP0(3),CLVL(3,1601,2),
     1   ZIMP(3),TBUF(3),PKB(3),CBUF(3,1601,2),HCBUF(3,1601,2),
     1   LPMI(3),LPIS(3),SMI(3,15),SIS(3,15),
     1   HMI(3,15),HIS(3,15),CMI(3,15),CIE(3,15),CIS(3,15),
     1   LMI(3,15,15),LIS(3,15,15),
     1   ATMI(3,15,1601),ATIS(3,15,1601),ATIE(3,15,1601),
     1   VI(3,1601,2),PI(3,1601,2),CI(3,15,1601,2),IMP(3,1601),
     1   LCHI(3,1601),XI(3,15,1601),FIVM(3,1601,2),FIKM(3,15,1601,2),
     1   FIVS(3,1601,2),FIKS(3,15,1601,2),CURI(3,1601),JV(3,1601,2),
     1   JK(3,15,1601,2)
C SPECIAL TRANSPORTERS
	CHARACTER*1 ISOFM
	DOUBLE PRECISION
     1   NP(3),KNPN(3),KNPK(3),KNH4(3),NPHK(3),
     1   LHP(3),XIHP(3),XHP(3),NAE1(3),NTSC(3),NNHE3(3),
     1   JNAK(3,3,1601,2),JHK(3,1601,2),JHP(3,1601,2),
     1   JAE1(3,1601,2),JTSC(3,1601,2),JNHE3(3,3,1601,2),QIAMM,
     1   NNKCC(3),NKCL(3),JNKCC(3,4,1601,2),JKCC(3,3,1601,2)
C
	DOUBLE PRECISION 
     1    PIKM(3,12,81),PJK(3,12,81),PIKS(3,12,81),
     1    CIKM(3,12,81),CJK(3,12,81),CIKS(3,12,81),
     1    GIKM(3,12,81),GJK(3,12,81),GIKS(3,12,81)
C
C	PIKM-	ACTIVE TRANSPORT OF SOLUTE ACROSS APICAL MEMBRANE
C	PJK-	ACTIVE TRANSPORT OF SOLUTE ACROSS LATERAL MEMBRANE
C	PIKS-	ACTIVE TRANSPORT OF SOLUTE ACROSS BASAL MEMBRANE
C	CIKM-	COTRANSPORT OF SOLUTE ACROSS APICAL MEMBRANE
C	CJK-	COTRANSPORT OF SOLUTE ACROSS LATERAL MEMBRANE
C	CIKS-	COTRANSPORT OF SOLUTE ACROSS BASAL MEMBRANE
C	GIKM-	CHANNEL FLUX OF SOLUTE ACROSS APICAL MEMBRANE
C	GJK-	CHANNEL FLUX OF SOLUTE ACROSS LATERAL MEMBRANE	
C	GIKS-	CHANNEL FLUX OF SOLUTE ACROSS BASAL MEMBRANE
C
	CHARACTER*5 SOL(12)
C
	DOUBLE PRECISION FLUXV(81),FLUXS(12,81),SCALE
C
C	FLUXV-	TOTAL EPITHELIAL VOLUME FLUX
C	FLUXS-	TOTAL EPITHELIAL SOLUTE FLUX
C	SCALE-  FACTOR TO TRANSLATE FROM MMOL/S*CM2 TO PMOL/MIN*MM
C		=PI*DIAM*(1.0E+09)*60/10
C			DIAM = 0.0030 cm  => SCALE = 5.65E+07
C			DIAM = 0.0016 cm  => SCALE = 3.02E+07
C
	DOUBLE PRECISION JNAHS(81),JCLB(81),JNAP(81),JNAB(81)
C
C
        COMMON SOLS,T,X,CHOP,
     1   Z,RT,RTE,F,
     1   EPSI,DT,RTAU,TIME,DIST,
     1   PKC,PKF,PKN,PKP,KHY,KDHY,L0,L,
     1   VM,PM,CM,IMPM,LCHM,XM,
     1   VS,PS,CS,IMPS,LCHS,XS,
     1   TL,DX,RM0,MUM,ETA,ZIMPS,
     1   SM,AM,FVM,FKM,
     1   AME,AE0,AE,MUA,CHVL0,CHVL,MUV,
     1   LPME,LPES,SME,SES,
     1   HME,HES,CME,CES,
     1   VE,PE,CE,LCHE,XE,
     1   FEVM,FEKM,FEVS,FEKS,CURE,
     1   AIE,AMI,AIS,AI0,CLVL0,IMP0,CLVL,
     1   ZIMP,TBUF,PKB,CBUF,HCBUF,
     1   LPMI,LPIS,SMI,SIS,
     1   HMI,HIS,CMI,CIE,CIS,
     1   LMI,LIS,ATMI,ATIS,ATIE,
     1   VI,PI,CI,IMP,LCHI,XI,
     1   FIVM,FIKM,FIVS,FIKS,CURI,
     1   JV,JK
	COMMON/KINET/ ISOFM,
     1   NP,KNPN,KNPK,KNH4,NPHK,
     1   LHP,XIHP,XHP,NAE1,NTSC,NNHE3,
     1   JNAK,JHK,JHP,JAE1,JTSC,JNHE3,QIAMM,
     1   NNKCC,NKCL,JNKCC,JKCC
C
C
	SOL(1)='  NA '
	SOL(2)='  K  '
	SOL(3)='  CL '
	SOL(4)=' HCO3'
	SOL(5)='H2CO3'
	SOL(6)=' CO2 '
	SOL(7)=' HPO4'
	SOL(8)='H2PO4'
	SOL(9)=' UREA'
	SOL(10)=' NH3 '
	SOL(11)=' NH4 '
	SOL(12)='  H  '
C
C SCALE FOR A 30 MICRON TUBULE DIAMETER
C	SCALE=5.65D+07
C SCALE FOR A 20 MICRON TUBULE DIAMETER
	SCALE=3.77D+07
C SCALE FOR A 16 MICRON TUBULE DIAMETER
C	SCALE=3.02D+07
C SCALE FOR A 15 MICRON TUBULE DIAMETER
C	SCALE=2.83D+07
C
	DO 570 KX=X-CHOP,X
	FEVM(KX,T)=FEVM(KX,T)*SCALE
	DO 520 I=1,SOLS
  520   FEKM(I,KX,T)=FEKM(I,KX,T)*SCALE
	DO 530 K=1,1
	FIVM(K,KX,T)=FIVM(K,KX,T)*SCALE
	DO 530 I=1,SOLS
  530   FIKM(K,I,KX,T)=FIKM(K,I,KX,T)*SCALE
C
	FLUXV(KX)=FEVM(KX,T)
	DO 550 I=1,SOLS
  550   FLUXS(I,KX)=FEKM(I,KX,T)
	DO 560 K=1,1
	FLUXV(KX)=FLUXV(KX) + FIVM(K,KX,T)
	DO 555 I=1,SOLS
  555   FLUXS(I,KX)=FLUXS(I,KX) + FIKM(K,I,KX,T)
  560	CONTINUE
  570	CONTINUE
C
C
	DO 170 KX=X-CHOP,X
C
C NET COTRANSPORTERS
	DO 110 K=1,1
	DO 110 I=1,SOLS
  110   CIKM(K,I,KX)=0.
C
	DO 120 K=1,1
	DO 120 I=1,SOLS
	DO 120 J=1,SOLS
  120   CIKM(K,I,KX)=CIKM(K,I,KX)+
     1    LMI(K,I,J)*AI0(K)*(XM(J,KX)-XI(K,J,KX))*SCALE
C
C CONDUCTANCE FLUXES
	DO 150 K=1,1
	DO 150 I=1,SOLS
  150   GIKM(K,I,KX)=FIKM(K,I,KX,T)-CIKM(K,I,KX)
C
  170   CONTINUE
C
C THE SOLUTE FLUXES ARE OUTPUT ACCORDING TO SOLUTE:
C   Na, K, Cl, NH4
C
	WRITE (127,300) 
  300   FORMAT(//,1X,'DIST',2X,'Na:',3X,'FLUX',6X,'FEKM',6X,'FIKM')
	DIST=DIST-TL-DX
	DO 310 KX=X-CHOP,X
	DIST=DIST+DX
  310   WRITE (127,320) DIST,FLUXS(1,KX),FEKM(1,KX,T),FIKM(1,1,KX,T)
  320   FORMAT (F7.4,2X,3F10.4)
C
	WRITE (127,330) 
  330   FORMAT(//,1X,'DIST',2X,'K:',5X,'FLUX',6X,'FEKM',6X,'FIKM')
	DIST=DIST-TL-DX
	DO 340 KX=X-CHOP,X
	DIST=DIST+DX
  340   WRITE (127,350) DIST,FLUXS(2,KX),FEKM(2,KX,T),FIKM(1,2,KX,T)
  350   FORMAT (F7.4,2X,3F10.4)
C
	WRITE (127,360) 
  360   FORMAT(//,1X,'DIST',2X,'Cl:',3X,'FLUX',6X,'FEKM',6X,'FIKM')
	DIST=DIST-TL-DX
	DO 370 KX=X-CHOP,X
	DIST=DIST+DX
  370   WRITE (127,380) DIST,FLUXS(3,KX),FEKM(3,KX,T),FIKM(1,3,KX,T)
  380   FORMAT (F7.4,2X,3F10.4)
C
	WRITE (127,390) 
  390   FORMAT(//,1X,'DIST',2X,'NH4:',3X,'FLUX',6X,'FEKM',6X,'FIKM',
     1   6X,'JNH3')
	DIST=DIST-TL-DX
	DO 400 KX=X-CHOP,X
	DIST=DIST+DX
  400   WRITE (127,410) DIST,FLUXS(11,KX),FEKM(11,KX,T),FIKM(1,11,KX,T),
     1  FIKM(1,10,X,T)
  410   FORMAT (F7.4,2X,4F10.4)
C
	DO 670 KX=X-CHOP,X
	FEVM(KX,T)=FEVM(KX,T)/SCALE
	DO 620 I=1,SOLS
  620   FEKM(I,KX,T)=FEKM(I,KX,T)/SCALE
	DO 630 K=1,1
	FIVM(K,KX,T)=FIVM(K,KX,T)/SCALE
	DO 630 I=1,SOLS
  630   FIKM(K,I,KX,T)=FIKM(K,I,KX,T)/SCALE
  670	CONTINUE
C
  700	CONTINUE
	RETURN
	END
