	SUBROUTINE IMCTGAM(SWG,GAMMA)
C PROGRAM TO SET AND RESET THE THE GAMMA ARRAY OF NN INDEPENDENT
C VARIABLES.  FOR SWG=1 THE GAMMA ARE DEFINED; FOR SWG=2, THE
C NAMED VARIABLES ARE RESET.
C
	INTEGER SOLS,T,SWG,X
C
C GENERAL PARAMETERS
	DOUBLE PRECISION
     1   Z(12),RT,RTE,F,
     1   EPSI,DT,RTAU,TIME,DIST,
     1   PKC,PKP,PKN,KHY(5),KDHY(5),L0,L(1601,2)
C LUMINAL AND PERITUBULAR PARAMETERS
	DOUBLE PRECISION
     1   VM(1601,2),PM(1601,2),CM(12,1601,2),
     1   IMPM(1601,2),LCHM(1601),XM(12,1601),
     1   VS(1601,2),PS(1601,2),CS(12,1601,2),
     1   IMPS(1601,2),LCHS(1601),XS(12,1601),ZIMPS,
     1   TL,DX,RM0,MUM,ETA,
     1   SM(1601,2),AM(1601,2),FVM(1601,2),FKM(13,1601,2)
C INTERSPACE PARAMETERS
	DOUBLE PRECISION
     1   AME,AE0,AE(1601,2),MUA,CHVL0,CHVL(1601,2),MUV,
     1   LPME,LPES,SME(12),SES(12),
     1   HME(12),HES(12),CME(12),CES(12),
     1   VE(1601,2),PE(1601,2),CE(12,1601,2),LCHE(1601),XE(12,1601),
     1   FEVM(1601,2),FEKM(12,1601,2),FEVS(1601,2),FEKS(12,1601,2),
     1   CURE(1601)
C CELL PARAMETERS
	DOUBLE PRECISION
     1   AIE(3),AI0(3),CLVL0(3),IMP0(3),CLVL(3,1601,2),
     1   ZIMP(3),TBUF(3),PKB(3),CBUF(3,1601,2),HCBUF(3,1601,2),
     1   LPMI(3),LPIS(3),SMI(3,12),SIS(3,12),
     1   HMI(3,12),HIS(3,12),CMI(3,12),CIE(3,12),CIS(3,12),
     1   LMI(3,12,12),LIS(3,12,12),ATMI(3,12,1601),ATIS(3,12,1601),
     1   VI(3,1601,2),PI(3,1601,2),CI(3,12,1601,2),IMP(3,1601),
     1   LCHI(3,1601),XI(3,12,1601),
     1   FIVM(3,1601,2),FIKM(3,12,1601,2),FIVS(3,1601,2),
     1   FIKS(3,12,1601,2),CURI(3,1601),JV(3,1601,2),JK(3,12,1601,2)
C SPECIAL TRANSPORTERS
	DOUBLE PRECISION
     1   NP(3),KNPN(3),KNPK(3),KNH4(3),NPHK(3),
     1   LHP(3),XIHP(3),XHP(3),NAE1(3),NTSC(3),NNHE3(3),
     1   JNAK(3,3,1601,2),JHK(3,1601,2),JHP(3,1601,2),
     1   JAE1(3,1601,2),JTSC(3,1601,2),JNHE3(3,3,1601,2)
C
	DOUBLE PRECISION GAMMA
	DIMENSION GAMMA(80)
C
        COMMON SOLS,T,X,
     1   Z,RT,RTE,F,
     1   EPSI,DT,RTAU,TIME,DIST,
     1   PKC,PKP,PKN,KHY,KDHY,L0,L,
     1   VM,PM,CM,IMPM,LCHM,XM,
     1   VS,PS,CS,IMPS,LCHS,XS,ZIMPS,
     1   TL,DX,RM0,MUM,ETA,
     1   SM,AM,FVM,FKM,
     1   AME,AE0,AE,MUA,CHVL0,CHVL,MUV,
     1   LPME,LPES,SME,SES,
     1   HME,HES,CME,CES,
     1   VE,PE,CE,LCHE,XE,
     1   FEVM,FEKM,FEVS,FEKS,CURE,
     1   AIE,AI0,CLVL0,IMP0,CLVL,
     1   ZIMP,TBUF,PKB,CBUF,HCBUF,
     1   LPMI,LPIS,SMI,SIS,
     1   HMI,HIS,CMI,CIE,CIS,
     1   LMI,LIS,ATMI,ATIS,
     1   VI,PI,CI,IMP,LCHI,XI,
     1   FIVM,FIKM,FIVS,FIKS,CURI,
     1   JV,JK
	COMMON/KINET/
     1   NP,KNPN,KNPK,KNH4,NPHK,
     1   LHP,XIHP,XHP,NAE1,NTSC,NNHE3,
     1   JNAK,JHK,JHP,JAE1,JTSC,JNHE3
C
C  SOLUTE INDEX:
C	1-	NA+
C	2-	K+
C	3-	CL-
C	4-	HCO3-
C	5-	H2CO3
C	6-	CO2
C	7-	HPO4--
C	8-	H2PO4-
C	9-	UREA
C	10-	NH3
C	11-	NH4+
C	12-	H+
C
C
	GO TO (100,200) SWG
C
C
  100   VE(X,T)=GAMMA(1)
	PE(X,T)=GAMMA(2)
	DO 136 I=1,SOLS
  136   CE(I,X,T)=GAMMA(2+I)
C RESCALE
	CE(5,X,T)=CE(5,X,T)*1.D-6
	CE(10,X,T)=CE(10,X,T)*1.D-6
	CE(11,X,T)=CE(11,X,T)*1.D-3
	CE(12,X,T)=CE(12,X,T)*1.D-9
C
	DO 145 K=1,1
	KK=1*(2+SOLS)
	VI(K,X,T)=GAMMA(KK+1)
	IMP(K,X)=GAMMA(KK+2)
	DO 140 I=1,SOLS
  140   CI(K,I,X,T)=GAMMA(KK+2+I)
C RESCALE
	CI(K,5,X,T)=CI(K,5,X,T)*1.D-6
	CI(K,10,X,T)=CI(K,10,X,T)*1.D-6
	CI(K,11,X,T)=CI(K,11,X,T)*1.D-3
	CI(K,12,X,T)=CI(K,12,X,T)*1.D-9
  145   CONTINUE
	KK=2*(2+SOLS)
	VM(X,T)=GAMMA(KK+1)
	PM(X,T)=GAMMA(KK+2)
	DO 150 I=1,SOLS
  150   CM(I,X,T)=GAMMA(KK+2+I)
C RESCALE
	CM(4,X,T)=CM(4,X,T)*1.D-3
	CM(5,X,T)=CM(5,X,T)*1.D-6
	CM(7,X,T)=CM(7,X,T)*1.D-3
	CM(10,X,T)=CM(10,X,T)*1.D-6
	CM(11,X,T)=CM(11,X,T)*1.D-3
	CM(12,X,T)=CM(12,X,T)*1.D-9
C
	FVM(X,T)=GAMMA(3*(2+SOLS)+1)*1.D-6
	IMPM(X,T)=GAMMA(3*(2+SOLS)+2)
	RETURN
C
C
  200   GAMMA(1)=VE(X,T)
	GAMMA(2)=PE(X,T)
	DO 236 I=1,SOLS
  236   GAMMA(2+I)=CE(I,X,T)
C SCALE
	GAMMA(2+5)=GAMMA(2+5)*1.D6
	GAMMA(2+10)=GAMMA(2+10)*1.D6
	GAMMA(2+11)=GAMMA(2+11)*1.D3
	GAMMA(2+12)=GAMMA(2+12)*1.D9
	DO 245 K=1,1
	KK=1*(2+SOLS)
	GAMMA(KK+1)=VI(K,X,T)
	GAMMA(KK+2)=IMP(K,X)
	DO 240 I=1,SOLS
  240   GAMMA(KK+2+I)=CI(K,I,X,T)
C SCALE
	GAMMA(KK+2+5)=GAMMA(KK+2+5)*1.D6
	GAMMA(KK+2+10)=GAMMA(KK+2+10)*1.D6
	GAMMA(KK+2+11)=GAMMA(KK+2+11)*1.D3
	GAMMA(KK+2+12)=GAMMA(KK+2+12)*1.D9
  245   CONTINUE
	KK=2*(2+SOLS)
	GAMMA(KK+1)=VM(X,T)
	GAMMA(KK+2)=PM(X,T)
	DO 250 I=1,SOLS
  250   GAMMA(KK+2+I)=CM(I,X,T)
C SCALE
	GAMMA(KK+2+4)=GAMMA(KK+2+4)*1.D3
	GAMMA(KK+2+5)=GAMMA(KK+2+5)*1.D6
	GAMMA(KK+2+7)=GAMMA(KK+2+7)*1.D3
	GAMMA(KK+2+10)=GAMMA(KK+2+10)*1.D6
	GAMMA(KK+2+11)=GAMMA(KK+2+11)*1.D3
	GAMMA(KK+2+12)=GAMMA(KK+2+12)*1.D9
C
	GAMMA(3*(2+SOLS)+1)=FVM(X,T)*1.D+6
	GAMMA(3*(2+SOLS)+2)=IMPM(X,T)
	RETURN
C
	END

