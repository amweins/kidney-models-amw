	PROGRAM REFORM
C  REFORM - PROGRAM TO READ THE DESIGNATED FILE IN E16.8 
C   REWRITE IT TO A DESIRED FORMAT
C
	REAL M
	DIMENSION M(3000,50)
	CHARACTER*19 IPTFIL,OPTFIL
C
	PRINT 5
    5   FORMAT(/,' PRINT THE OUTPUT FILENAME:',$)
	READ 4, OPTFIL
    4   FORMAT (A19)
	OPEN(2,FILE=OPTFIL)
C
	PRINT 2
    2   FORMAT(/,' PRINT THE INPUT FILENAME:',$)
	READ 4, IPTFIL
	OPEN(1,FILE=IPTFIL)
C
	PRINT 10, IPTFIL
   10   FORMAT(' PRINT THE NUMBER OF COLUMNS IN ',A19,$)
	READ *, NCOLS
	DO 20 I=1,3000
   20   READ(1,30,END=40) (M(I,J),J=1,NCOLS)
   30   FORMAT(8E16.8)
   40   NROW=I-1
	CLOSE(1)
C
	DO 340 I=1,NROW
  340   WRITE(2,350) (M(I,J),J=1,NCOLS)
C  350   FORMAT(8E12.4)
  350   FORMAT(12F8.3)
C
	STOP
	END
