	SUBROUTINE BRESUL
C
	INTEGER SOLS,T
C
C GENERAL PARAMETERS
	DOUBLE PRECISION
     1   Z(12),RT,RTE,F,
     1   EPSI,DT,RTAU,TIME,
     1   PKC,PKP,PKN,KHY(4),KDHY(4),L0,L(2)
C LUMINAL AND PERITUBULAR PARAMETERS
	DOUBLE PRECISION
     1   DUMVM,VM,PM,CM(12),IMPM,LCHM,XM(12),
     1   VS,PS,CS(12),IMPS,LCHS,XS(12)
C INTERSPACE PARAMETERS
	DOUBLE PRECISION
     1   AME,AE0,AE(2),MUA,CHVL0,CHVL(2),MUV,
     1   LPME,LPES,SME(12),SES(12),
     1   HME(12),HES(12),CME(12),CES(12),
     1   VE(2),PE(2),CE(12,2),LCHE,XE(12),
     1   FEVM(2),FEKM(12,2),FEVS(2),FEKS(12,2),CURE
C CELL PARAMETERS
	DOUBLE PRECISION
     1   AIE(3),AI0(3),CLVL0(3),IMP0(3),CLVL(3,2),
     1   ZIMP(3),TBUF(3),PKB(3),CBUF(3,2),HCBUF(3,2),
     1   LPMI(3),LPIS(3),SMI(3,12),SIS(3,12),
     1   HMI(3,12),HIS(3,12),CMI(3,12),CIE(3,12),CIS(3,12),
     1   LMI(3,12,12),LIS(3,12,12),ATMI(3,12),ATIS(3,12),ATIE(3,12),
     1   VI(3,2),PI(3,2),CI(3,12,2),IMP(3),LCHI(3),XI(3,12),
     1   FIVM(3,2),FIKM(3,12,2),FIVS(3,2),FIKS(3,12,2),CURI(3),
     1   JV(3,2),JK(3,12,2)
C SPECIAL TRANSPORTERS
	DOUBLE PRECISION
     1   NP(3),KNPN(3),KNPK(3),KNH4(3),NPHK(3),
     1   LHP(3),XIHP(3),XHP(3),NAE1(3),NTSC(3),NNHE3(3),
     1   JNAK(3,3),JHK(3),JHP(3),JAE1(3),JTSC(3),JNHE3(3,3),
     1   NNKCC(3),NKCL(3),JNKCC(3,4),JKCC(3,3)
C
	CHARACTER*5 SOL(12)
C
	DOUBLE PRECISION QE(12),QI(3,12),QEV,QIV(3),
     1  OSME,OSMI(3),OSMM,OSMS,FLUXV,FLUXS(12)
C
C
C       QE-     DERIVATIVE OF SPECIES CONTENT OF THE CHANNEL (MMOL/S)
C       QI-     DERIVATIVE OF SPECIES CONTENT OF THE CELL (MMOL/S)
C       QEV-    DERIVATIVE VOLUME CONTENT OF THE CHANNEL (ML/S)
C       QIV-    DERIVATIVE VOLUME CONTENT OF THE  CELL (ML/S)
C       OSM.-   OSMOLALITY OF INDICATED COMPARTMENT
C	       .= E,I,M,S
C	FLUXV-	TOTAL EPITHELIAL VOLUME FLUX
C	FLUXS-	TOTAL EPITHELIAL SOLUTE FLUX
C
        COMMON SOLS,T,
     1   Z,RT,RTE,F,
     1   EPSI,DT,RTAU,TIME,
     1   PKC,PKP,PKN,KHY,KDHY,L0,L,
     1   DUMVM,VM,PM,CM,IMPM,LCHM,XM,
     1   VS,PS,CS,IMPS,LCHS,XS,
     1   AME,AE0,AE,MUA,CHVL0,CHVL,MUV,
     1   LPME,LPES,SME,SES,
     1   HME,HES,CME,CES,
     1   VE,PE,CE,LCHE,XE,
     1   FEVM,FEKM,FEVS,FEKS,CURE,
     1   AIE,AI0,CLVL0,IMP0,CLVL,
     1   ZIMP,TBUF,PKB,CBUF,HCBUF,
     1   LPMI,LPIS,SMI,SIS,
     1   HMI,HIS,CMI,CIE,CIS,
     1   LMI,LIS,ATMI,ATIS,ATIE,
     1   VI,PI,CI,IMP,LCHI,XI,
     1   FIVM,FIKM,FIVS,FIKS,CURI,
     1   JV,JK
	COMMON/KINET/
     1   NP,KNPN,KNPK,KNH4,NPHK,
     1   LHP,XIHP,XHP,NAE1,NTSC,NNHE3,
     1   JNAK,JHK,JHP,JAE1,JTSC,JNHE3,
     1   NNKCC,NKCL,JNKCC,JKCC
C
C
	SOL(1)='  NA '
	SOL(2)='  K  '
	SOL(3)='  CL '
	SOL(4)=' HCO3'
	SOL(5)='H2CO3'
	SOL(6)=' CO2 '
	SOL(7)=' HPO4'
	SOL(8)='H2PO4'
	SOL(9)=' UREA'
	SOL(10)=' NH3 '
	SOL(11)=' NH4 '
	SOL(12)='  H  '
C
C
C DERIVED QUANTITIES ARE COMPUTED:OSMOLALITY,CURRENT FLOW,AND THE
C NET QUANTITY "Q(I)" OF SOLUTE INTRODUCED INTO THE COMPARTMENT
C
	LCHM=-1.*DLOG10(CM(SOLS))
	LCHS=-1.*DLOG10(CS(SOLS))
C
	OSMM=IMPM
	OSMS=IMPS
	DO 320 I=1,SOLS
	OSMM=OSMM+CM(I)
	OSMS=OSMS+CS(I)
  320   CONTINUE
C
	CURE=0.D0
	DO 340 K=1,1
  340   CURI(K)=0.D0
	DO 345 I=1,SOLS
	CURE=CURE+F*Z(I)*FEKM(I,T)
	DO 345 K=1,1
  345   CURI(K)=CURI(K)+F*Z(I)*FIKM(K,I,T)
C
C
	WRITE (21,396) TIME
  396   FORMAT (1H1,/,5X,'TIME=',F9.4)
C
	WRITE (21,400) VM
	WRITE (21,402) PM,PS
C
	WRITE (21,410) (SOL(I),CM(I),CS(I),XM(I)-XS(I),I=1,4)	
	WRITE (21,412) (SOL(I),CM(I),CS(I),XM(I)-XS(I),I=5,5)	
	WRITE (21,414) (SOL(I),CM(I),CS(I),XM(I)-XS(I),I=6,SOLS-3)	
	WRITE (21,412) (SOL(I),CM(I),CS(I),XM(I)-XS(I),I=SOLS-2,SOLS-2)	
	WRITE (21,414) (SOL(I),CM(I),CS(I),XM(I)-XS(I),I=SOLS-1,SOLS-1)	
	WRITE (21,412) SOL(SOLS),CM(SOLS),CS(SOLS),XM(SOLS)-XS(SOLS)
	WRITE (21,420) IMPM,IMPS
	WRITE (21,430) OSMM,OSMS
	WRITE (21,440) LCHM,LCHS
	WRITE (21,442) 
C
	WRITE (21,470) (FIVM(K,T),K=1,1),FEVM(T)
	WRITE (21,480) (SOL(I),(FIKM(K,I,T),K=1,1),FEKM(I,T),I=1,SOLS)
	WRITE (21,485) (CURI(K),K=1,1),CURE
C
C
  400   FORMAT(//,16X,'VM', /,8X,F12.3)
  402   FORMAT(//,16X,'PM',10X,'PS',/,8X,2F12.3)
C
  410   FORMAT(//,16X,'CM',10X,'CS',9X,'XM-XS',/,(4X,A5,3F12.6))
  412   FORMAT(4X,A5,1X,2D12.4,F11.6)
  414   FORMAT(4X,A5,3F12.6)
  420   FORMAT(5X,'IMP',1X,2F12.6)
  430   FORMAT(5X,'OSM',1X,2F12.6)
  440   FORMAT(6X,'PH',1X,2F12.6)
  442   FORMAT(//)
C
  470   FORMAT(1H1,//,14X,'FIVM ',7X,'FEVM ',/,9X,2D12.4)
  480   FORMAT(//,14X, 'FIKM ',7X,'FEKM ',/,(4X,A5,2D12.4))
  485   FORMAT(4X,'CUR',2X,2D12.4)
C
C
C OUTPUT FOR AN AUXILLIARY FILE--E.G. PERMEABILITY CALCULATIONS
C
	FLUXV=FEVM(T)
	DO 550 I=1,SOLS
  550   FLUXS(I)=FEKM(I,T)
	DO 560 K=1,1
	FLUXV=FLUXV + FIVM(K,T)
	DO 555 I=1,SOLS
  555   FLUXS(I)=FLUXS(I) + FIKM(K,I,T)
  560	CONTINUE
C
	WRITE (23,570) FLUXV,(FLUXS(I),I=1,4),
     1   FLUXS(6),FLUXS(9),FLUXS(11),FLUXS(10)
  570   FORMAT (8E16.8,/,8E16.8)
C
	RETURN
	END
