	SUBROUTINE GAMSET(SWG,GAMMA)
C PROGRAM TO SET AND RESET THE THE GAMMA ARRAY OF NN INDEPENDENT
C VARIABLES.  FOR SWG=1 THE GAMMA ARE DEFINED; FOR SWG=2, THE
C NAMED VARIABLES ARE RESET.
C
	INTEGER SOLS,T,SWG
C
C GENERAL PARAMETERS
	DOUBLE PRECISION
     1   Z(12),RT,RTE,F,
     1   EPSI,DT,RTAU,TIME,
     1   PKC,PKP,PKN,KHY(4),KDHY(4),L0,L(2)
C LUMINAL AND PERITUBULAR PARAMETERS
	DOUBLE PRECISION
     1   DUMVM,VM,PM,CM(12),IMPM,LCHM,XM(12),
     1   VS,PS,CS(12),IMPS,LCHS,XS(12)
C INTERSPACE PARAMETERS
	DOUBLE PRECISION
     1   AME,AE0,AE(2),MUA,CHVL0,CHVL(2),MUV,
     1   LPME,LPES,SME(12),SES(12),
     1   HME(12),HES(12),CME(12),CES(12),
     1   VE(2),PE(2),CE(12,2),LCHE,XE(12),
     1   FEVM(2),FEKM(12,2),FEVS(2),FEKS(12,2),CURE
C CELL PARAMETERS
	DOUBLE PRECISION
     1   AIE(3),AI0(3),CLVL0(3),IMP0(3),CLVL(3,2),
     1   ZIMP(3),TBUF(3),PKB(3),CBUF(3,2),HCBUF(3,2),
     1   LPMI(3),LPIS(3),SMI(3,12),SIS(3,12),
     1   HMI(3,12),HIS(3,12),CMI(3,12),CIE(3,12),CIS(3,12),
     1   LMI(3,12,12),LIS(3,12,12),ATMI(3,12),ATIS(3,12),
     1   NP(3),KNPN(3),KNPK(3),KNH4(3),NPHK(3),
     1   LHP(3),XIHP(3),XHP(3),NAE1(3),
     1   VI(3,2),PI(3,2),CI(3,12,2),IMP(3),LCHI(3),XI(3,12),
     1   FIVM(3,2),FIKM(3,12,2),FIVS(3,2),FIKS(3,12,2),CURI(3),
     1   JV(3,2),JK(3,12,2)
C
	DOUBLE PRECISION GAMMA
	DIMENSION GAMMA(60)
C
C
        COMMON SOLS,T,
     1   Z,RT,RTE,F,
     1   EPSI,DT,RTAU,TIME,
     1   PKC,PKP,PKN,KHY,KDHY,L0,L,
     1   DUMVM,VM,PM,CM,IMPM,LCHM,XM,
     1   VS,PS,CS,IMPS,LCHS,XS,
     1   AME,AE0,AE,MUA,CHVL0,CHVL,MUV,
     1   LPME,LPES,SME,SES,
     1   HME,HES,CME,CES,
     1   VE,PE,CE,LCHE,XE,
     1   FEVM,FEKM,FEVS,FEKS,CURE,
     1   AIE,AI0,CLVL0,IMP0,CLVL,
     1   ZIMP,TBUF,PKB,CBUF,HCBUF,
     1   LPMI,LPIS,SMI,SIS,
     1   HMI,HIS,CMI,CIE,CIS,
     1   LMI,LIS,ATMI,ATIS,
     1   NP,KNPN,KNPK,KNH4,NPHK,
     1   LHP,XIHP,XHP,NAE1,
     1   VI,PI,CI,IMP,LCHI,XI,
     1   FIVM,FIKM,FIVS,FIKS,CURI,
     1   JV,JK
C
C  SOLUTE INDEX:
C	1-	NA+
C	2-	K+
C	3-	CL-
C	4-	HCO3-
C	5-	H2CO3
C	6-	CO2
C	7-	HPO4--
C	8-	H2PO4-
C	9-	UREA
C	10-	NH3
C	11-	NH4+
C	12-	H+
C
C   NN=4*(2+SOLS)+SW1
C
C
	GO TO (100,200) SWG
C
C
  100   VE(T)=GAMMA(1)
	PE(T)=GAMMA(2)
	DO 136 I=1,SOLS
  136   CE(I,T)=GAMMA(2+I)
C RESCALE
	CE(5,T)=CE(5,T)*1.D-6
	CE(10,T)=CE(10,T)*1.D-6
	CE(11,T)=CE(11,T)*1.D-3
	CE(12,T)=CE(12,T)*1.D-9
C
	DO 145 K=1,1
	KK=1*(2+SOLS)
	VI(K,T)=GAMMA(KK+1)
	IMP(K)=GAMMA(KK+2)
	DO 140 I=1,SOLS
  140   CI(K,I,T)=GAMMA(KK+2+I)
C RESCALE
	CI(K,5,T)=CI(K,5,T)*1.D-6
	CI(K,10,T)=CI(K,10,T)*1.D-6
	CI(K,11,T)=CI(K,11,T)*1.D-3
	CI(K,12,T)=CI(K,12,T)*1.D-9
  145   CONTINUE
	VM=GAMMA(2*(2+SOLS)+1)
	RETURN
C
C
  200   GAMMA(1)=VE(T)
	GAMMA(2)=PE(T)
	DO 236 I=1,SOLS
  236   GAMMA(2+I)=CE(I,T)
C SCALE
	GAMMA(2+5)=GAMMA(2+5)*1.D6
	GAMMA(2+10)=GAMMA(2+10)*1.D6
	GAMMA(2+11)=GAMMA(2+11)*1.D3
	GAMMA(2+12)=GAMMA(2+12)*1.D9
	DO 245 K=1,1
	KK=1*(2+SOLS)
	GAMMA(KK+1)=VI(K,T)
	GAMMA(KK+2)=IMP(K)
	DO 240 I=1,SOLS
  240   GAMMA(KK+2+I)=CI(K,I,T)
C SCALE
	GAMMA(KK+2+5)=GAMMA(KK+2+5)*1.D6
	GAMMA(KK+2+10)=GAMMA(KK+2+10)*1.D6
	GAMMA(KK+2+11)=GAMMA(KK+2+11)*1.D3
	GAMMA(KK+2+12)=GAMMA(KK+2+12)*1.D9
  245   CONTINUE
	GAMMA(2*(2+SOLS)+1)=VM
	RETURN
C
	END

