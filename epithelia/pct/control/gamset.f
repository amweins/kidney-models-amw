	SUBROUTINE GAMSET(SWG,GAMMA)
C PROGRAM TO SET AND RESET THE THE GAMMA ARRAY OF NN INDEPENDENT
C VARIABLES.  FOR SWG=1 THE GAMMA ARE DEFINED; FOR SWG=2, THE
C NAMED VARIABLES ARE RESET.
C
	INTEGER SOLS,T,TAU,TLIM,SWG
C
C GENERAL PARAMETERS
	DOUBLE PRECISION
     1   Z(15),RT,RTE,F,
     1   EPSI,DT,RTAU,TIME,L0,L(2),
     1   PKC,PKF,PKN,PKP,KHY(4),KDHY(4)
C LUMINAL AND PERITUBULAR PARAMETERS
	DOUBLE PRECISION
     1   DUMVM,VM,PM,CM(15),IMPM,LCHM,XM(15),
     1   VS,PS,CS(15),IMPS,LCHS,XS(15)
C INTERSPACE PARAMETERS
	DOUBLE PRECISION
     1   AME,AE0,AE(2),MUA,CHVL0,CHVL(2),MUV,
     1   LPME,LPES,SME(15),SES(15),
     1   HME(15),HES(15),CME(15),CES(15),
     1   VE(2),PE(2),CE(15,2),LCHE,XE(15),
     1   FEVM(2),FEKM(15,2),FEVS(2),FEKS(15,2),CURE
C CELL PARAMETERS
	DOUBLE PRECISION
     1   AIE(3),AMI(3),AIS(3),CLVL0(3),IMP0(3),CLVL(3,2),
     1   ZIMP(3),TBUF(3),PKB(3),CBUF(3,2),HCBUF(3,2),
     1   LPMI(3),LPIS(3),SMI(3,15),SIS(3,15),
     1   HMI(3,15),HIS(3,15),CMI(3,15),CIE(3,15),CIS(3,15),
     1   LMI(3,15,15),LIS(3,15,15),ATMI(3,15),ATIS(3,15),
     1   VI(3,2),PI(3,2),CI(3,15,2),IMP(3),LCHI(3),XI(3,15),
     1   FIVM(3,2),FIKM(3,15,2),FIVS(3,2),FIKS(3,15,2),CURI(3),
     1   JV(3,2),JK(3,15,2)
C SPECIAL TRANSPORTERS
	DOUBLE PRECISION
     1   NP(3),KNPN(3),KNPK(3),KNH4(3),NPHK(3),
     1   LHP(3),XIHP(3),XHP(3),NAE1(3),NTSC(3),NNHE3(3),
     1   JNAK(3,3),JHK(3),JHP(3),JAE1(3),JTSC(3),JNHE3(3,3),
     1   QIAMM
C COTRANSPORTER INFORMATION
	INTEGER  
     1   NMI,ORDMI(9),IMI(9,9),JMI(9,9),MMI(9,9),
     1   NIS,ORDIS(9),IIS(9,9),JIS(9,9),MIS(9,9)
	DOUBLE PRECISION RMI(9),RIS(9)
C CONTROL VARIABLES
	DOUBLE PRECISION 
     1   HCON(10),HCON0(10),DHCON(10,4),CON(4),CON0(10,4)
C
	DOUBLE PRECISION GAMMA
	DIMENSION GAMMA(60)
C
C
        COMMON SOLS,T,TAU,TLIM,
     1   Z,RT,RTE,F,
     1   EPSI,DT,RTAU,TIME,
     1   PKC,PKF,PKN,PKP,KHY,KDHY,L0,L,
     1   DUMVM,VM,PM,CM,IMPM,LCHM,XM,
     1   VS,PS,CS,IMPS,LCHS,XS,
     1   AME,AE0,AE,MUA,CHVL0,CHVL,MUV,
     1   LPME,LPES,SME,SES,
     1   HME,HES,CME,CES,
     1   VE,PE,CE,LCHE,XE,
     1   FEVM,FEKM,FEVS,FEKS,CURE,
     1   AIE,AMI,AIS,CLVL0,IMP0,CLVL,
     1   ZIMP,TBUF,PKB,CBUF,HCBUF,
     1   LPMI,LPIS,SMI,SIS,
     1   HMI,HIS,CMI,CIE,CIS,
     1   LMI,LIS,ATMI,ATIS,
     1   VI,PI,CI,IMP,LCHI,XI,
     1   FIVM,FIKM,FIVS,FIKS,CURI,
     1   JV,JK
	COMMON/KINET/
     1   NP,KNPN,KNPK,KNH4,NPHK,
     1   LHP,XIHP,XHP,NAE1,NTSC,NNHE3,
     1   JNAK,JHK,JHP,JAE1,JTSC,JNHE3,QIAMM
	COMMON/COTR/
     1   NMI,ORDMI,IMI,JMI,MMI,
     1   NIS,ORDIS,IIS,JIS,MIS,RMI,RIS,
     1   HCON,HCON0,DHCON,CON,CON0
C
C  SOLUTE INDEX:
C	1-	NA+
C	2-	K+
C	3-	CL-
C	4-	HCO3-
C	5-	H2CO3
C	6-	CO2
C	7-	HPO4--
C	8-	H2PO4-
C	9-	UREA
C	10-	NH3
C	11-	NH4+
C	12-	H+
C	13-	HCO2-
C	14-	H2CO2
C	15-	GLUCOSE
C
C   NN=2*SOLS+4+SW1
C
C
	GO TO (100,200) SWG
C
C
  100   VE(T)=GAMMA(1)
	PE(T)=GAMMA(2)
	DO 136 I=1,11
  136   CE(I,T)=GAMMA(2+I)
	DO 138 I=13,SOLS
  138   CE(I,T)=GAMMA(1+I)
C RESCALE
	CE(5,T)=CE(5,T)*1.D-3
	CE(10,T)=CE(10,T)*1.D-3
	CE(14,T)=CE(14,T)*1.D-5
C
	DO 145 K=1,1
	KK=SOLS+1
	VI(K,T)=GAMMA(KK+1)
	IMP(K)=GAMMA(KK+2)
	DO 140 I=1,11
  140   CI(K,I,T)=GAMMA(KK+2+I)
	DO 142 I=13,SOLS
  142   CI(K,I,T)=GAMMA(KK+1+I)
C
	CBUF(K,T)=GAMMA(2*KK+1)
	HCBUF(K,T)=GAMMA(2*KK+2)
C RESCALE
	CI(K,5,T)=CI(K,5,T)*1.D-3
	CI(K,10,T)=CI(K,10,T)*1.D-3
	CI(K,14,T)=CI(K,14,T)*1.D-5
  145   CONTINUE
	VM=GAMMA(2*SOLS+5)
	RETURN
C
C
  200   GAMMA(1)=VE(T)
	GAMMA(2)=PE(T)
	DO 236 I=1,11
  236   GAMMA(2+I)=CE(I,T)
	DO 238 I=13,SOLS
  238   GAMMA(1+I)=CE(I,T)
C SCALE
	GAMMA(7)=GAMMA(7)*1.D3
	GAMMA(12)=GAMMA(12)*1.D3
	GAMMA(15)=GAMMA(15)*1.D5
C
	DO 245 K=1,1
	KK=SOLS+1
	GAMMA(KK+1)=VI(K,T)
	GAMMA(KK+2)=IMP(K)
	DO 240 I=1,11
  240   GAMMA(KK+2+I)=CI(K,I,T)
	DO 242 I=13,SOLS
  242   GAMMA(KK+1+I)=CI(K,I,T)
C
	GAMMA(2*KK+1)=CBUF(K,T)
	GAMMA(2*KK+2)=HCBUF(K,T)
C SCALE
	GAMMA(KK+7)=GAMMA(KK+7)*1.D3
	GAMMA(KK+12)=GAMMA(KK+12)*1.D3
	GAMMA(KK+15)=GAMMA(KK+15)*1.D5
  245   CONTINUE
	GAMMA(2*SOLS+5)=VM
	RETURN
C
	END

