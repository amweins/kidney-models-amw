	SUBROUTINE MRDPSET(TAU,TLIM)
C PROGRAM TO READ AND INITIALIZE THE PARAMETERS FOR COMP
C
	INTEGER SOLS,TAU,T,TLIM,X,CHOP
C
	DOUBLE PRECISION
     1   Z(15),ZIMPC,RT,RTE,F,
     1   EPSI,SCALE,DT,RTAU,TIME,DIST,
     1   PKC,PKP,PKN,PKF,KHY,KDHY,
     1   LPCS,SCS(15),LCS(15),HCS(15),
     1   LPCSE,SCSE(15),LCSE(15),HCSE(15),
     1   LPCSI,SCSI(15),LCSI(15),HCSI(15),
     1   CL,DX,RC0,MUC,ETA,
     1   KXO,KXD,KYO,KYD,
     1   KCO,KCD,KZO,KZD,
     1   A0,A1,A2,P50,HILL,BOHR
C 
	DOUBLE PRECISION
     1   VC(901,2),PC(901,2),CC(31,901,2),IMPC(901,2),
     1   LCHC(901),XC(15,901),
     1   VS(901,2),PS(901,2),CS(16,901,2),
     1   IMPS(901,2),LCHS(901),XS(15,901),
     1   CCS(15),SAT(901,2),CCN(901,2),CCX(901,2),CCY(901,2),
     1   SC(901,2),AC(901,2),FVC(901,2),FKC(31,901,2),
     1   QV,QC(31),FVCS(901,2),FKCS(16,901,2),
     1   FVCSE(901,2),FKCSE(16,901,2),FVCSI(901,2),FKCSI(16,901,2),
     1   HCTC(901,2),FBC(901,2),POX(901,2)
C
C
	COMMON/PAR/ SOLS,T,X,CHOP,
     1   Z,ZIMPC,RT,RTE,F,
     1   EPSI,SCALE,DT,RTAU,TIME,DIST,
     1   PKC,PKP,PKN,PKF,KHY,KDHY,
     1   LPCS,SCS,LCS,HCS,
     1   LPCSE,SCSE,LCSE,HCSE,
     1   LPCSI,SCSI,LCSI,HCSI,
     1   CL,DX,RC0,MUC,ETA,
     1   KXO,KXD,KYO,KYD,
     1   KCO,KCD,KZO,KZD,
     1   A0,A1,A2,P50,HILL,BOHR
	COMMON/VAR/
     1   VC,PC,CC,VS,PS,CS,IMPC,IMPS,
     1   LCHC,LCHS,XC,XS,CCS,
     1   SAT,CCN,CCX,CCY,HCTC,FBC,POX,
     1   SC,AC,FVC,FKC,QC,QV,FVCS,FKCS,
     1   FVCSE,FKCSE,FVCSI,FKCSI
C
C  SOLUTE INDEX:
C	1-          NA+
C	2-          K+
C	3-          CL-
C	4-          HCO3-
C	5-          H2CO3
C	6-          CO2
C	7<-5        HPO4--
C	8<-6        H2PO4-
C	9<-8        UREA
C	10-         NH3
C	11-         NH4+
C	12<-9       H+
C	13-         HCO2-
C	14-         H2CO2
C	15<-7       GLUC
C	16<-10      PROT
C
C	17<-11      ONH2 
C       18<-12      NH2  
C	19<-13      ONH3+
C	20<-14      NH3+ 
C	21<-15      ONHCO2-
C	22<-16      NHCO2-
C	23<-17      OXH 
C	24<-18      XH   
C	25<-19      OX-  
C	26<-20      X-   
C	27<-21      OYH  
C	28<-22      YH   
C	29<-23      OY-  
C	30<-24      Y-   
C
C
	Z(1)=1.D0
	Z(2)=1.D0
	Z(3)=-1.D0
	Z(4)=-1.D0
	Z(5)=0.D0
	Z(6)=0.D0
	Z(7)=-2.D0
	Z(8)=-1.D0
	Z(9)=0.D0
	Z(10)=0.D0
	Z(11)=1.D0
	Z(12)=1.D0
	Z(13)=-1.D0
	Z(14)=0.D0
	Z(15)=0.D0
C
	RT=0.193D+05
	RTE=2.57
	F=0.965000D+05
	PKC=3.57
	PKF=3.76
	PKP=6.8
	PKN=9.15
        KHY=1.45D+03
        KDHY=4.96D+05
	SCALE=1.D0
C
	KXO=2.14D-7
	KXD=1.00D-7
	KYO=1.45D-8
	KYD=8.13D-9
	KCO=1.13D-5
	KCD=2.15D-5
	KZO=1.79D-8
	KZD=3.98D-8
	A0=2.1
	A1=.16
	A2=.009
	P50=36.0
	HILL=2.60
	BOHR= -0.6
C
C
C GEOMETRIC AND MEMBRANE PROPERTIES ARE READ. 
C
	READ (241,30) TAU,TLIM,CHOP,EPSI,
     1  CL,RC0,MUC,ETA,ZIMPC,LPCSE,
     1  (SCSE(I),HCSE(I),I=1,SOLS)
   30   FORMAT (3I5,D9.1/,5D12.4,/,D12.4,/,(F6.3,D12.4))
C
	READ (241,26) LPCSI,
     1  (SCSI(I),HCSI(I),I=1,SOLS)
   26   FORMAT (D12.4,/,(F6.3,D12.4))
C
	ETA=ETA*SCALE
        LPCS = LPCSE + LPCSI
        DO 50 I=1,SOLS
        SCS(I) = (SCSE(I)*LPCSE + SCSI(I)*LPCSI)/(LPCSE+LPCSI)
        HCS(I) = HCSE(I) + HCSI(I)
   50   CONTINUE
C
C
	RETURN
	END
